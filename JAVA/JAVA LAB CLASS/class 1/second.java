public class second {

    public static void main(String[] args) {

        int a = 100;
        double b = 99.99;
        char c = 'A';
        String x = "Java";
        boolean y = true;

        System.out.printf("Integer: %d\n", a);
        System.out.printf("Double: %.2f\n", b);
        System.out.printf("Character: %c\n", c);
        System.out.printf("String: %s\n", x);
        System.out.printf("Boolean: %b\n", y);

    }
}
